export const getDummyResponse = (
  message: string,
  hasImage: boolean = false
): string => {
  const lowerMessage = message.toLowerCase();

  // If image is present, prioritize image-related responses
  if (hasImage) {
    if (
      lowerMessage.includes("portfolio") ||
      lowerMessage.includes("balance") ||
      lowerMessage.includes("holdings")
    ) {
      return dummyResponses.imagePortfolio.content;
    }

    if (
      lowerMessage.includes("transaction") ||
      lowerMessage.includes("tx") ||
      lowerMessage.includes("transfer")
    ) {
      return dummyResponses.imageTransaction.content;
    }

    if (
      lowerMessage.includes("chart") ||
      lowerMessage.includes("price") ||
      lowerMessage.includes("analysis")
    ) {
      return dummyResponses.imageChart.content;
    }

    if (
      lowerMessage.includes("nft") ||
      lowerMessage.includes("collection") ||
      lowerMessage.includes("token")
    ) {
      return dummyResponses.imageNFT.content;
    }

    // Default image response
    return dummyResponses.image.content;
  }

  // Regular text-based responses
  if (lowerMessage.includes("portfolio") || lowerMessage.includes("balance")) {
    return dummyResponses.portfolio.content;
  }

  if (lowerMessage.includes("send") || lowerMessage.includes("transfer")) {
    return dummyResponses.send.content;
  }

  if (
    lowerMessage.includes("analyze") ||
    lowerMessage.includes("performance") ||
    lowerMessage.includes("price")
  ) {
    return dummyResponses.analyze.content;
  }

  return dummyResponses.default.content;
};
